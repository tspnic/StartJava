package com.startjava.lesson1.variable;

public class Variable {

	public static void main(String[] args) {
		byte numberCores = 2;
		System.out.println("Number of Cores = " + numberCores);
		short numberWindows = 10;
		System.out.println("Windows " + numberWindows + "Pro");
		int sizeExternalDisk = 1000;
		System.out.println("External Drive = " + sizeExternalDisk + "GB");
		long sizeHardDisk = 320;
		System.out.println("HDD = " + sizeHardDisk + "GB");
		float ram = 4.0F;
		System.out.println("RAM = " + ram + "GB");
		double frequencyCpu = 2.2;
		System.out.println("CPU frequency = " + frequencyCpu + "GHz");
		char systemCapacity = '@';
		System.out.println("system capacity = " + (int) systemCapacity + "Bit");
		boolean numberCores1 = true;
		System.out.println("Number of Cores = " + numberCores);
	}
}